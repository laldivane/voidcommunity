-- ════════════════════════════
-- 6. TRIGGERS FOR COUNTERS
-- ════════════════════════════

-- Trigger for play_count
CREATE OR REPLACE FUNCTION public.handle_play_count()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE public.tracks SET play_count = play_count + 1 WHERE id = NEW.track_id;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_track_played ON public.listening_activity;
CREATE TRIGGER on_track_played
  AFTER INSERT ON public.listening_activity
  FOR EACH ROW EXECUTE FUNCTION public.handle_play_count();

-- Trigger for like_count
CREATE OR REPLACE FUNCTION public.handle_like_count()
RETURNS TRIGGER AS $$
BEGIN
  IF (TG_OP = 'INSERT') THEN
    UPDATE public.tracks SET like_count = like_count + 1 WHERE id = NEW.track_id;
  ELSIF (TG_OP = 'DELETE') THEN
    UPDATE public.tracks SET like_count = like_count - 1 WHERE id = OLD.track_id;
  END IF;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_track_liked ON public.favorites;
CREATE TRIGGER on_track_liked
  AFTER INSERT OR DELETE ON public.favorites
  FOR EACH ROW EXECUTE FUNCTION public.handle_like_count();
